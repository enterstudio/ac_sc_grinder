void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* AdcHandle)
{
    //uint16_t g_ADCValue = HAL_ADC_GetValue(AdcHandle);
    
    uint16_t g_ADCValue = gADCBuffer[0];
    uint16_t g_ADCCurrent = gADCBuffer[1];
    
    current_accum += g_ADCCurrent;
    current_accum_counter++;
    
    if (simistor_counter>control_angle)
    {
        voltage_accum += g_ADCValue;
        voltage_accum_counter++;
    }
    
    if (((g_ADCValue != 0)&&(prev_ADCvalue == 0))||((g_ADCValue == 0)&&(prev_ADCvalue != 0)))
    {
        if (g_ADCValue == 0)
        {
            uint16_t current_value = current_accum/current_accum_counter;
            uint16_t R_ekv;
            
            if (current_accum!=0) 
                R_ekv = voltage_accum*256*16/current_accum;
            else
                R_ekv = 0;
            
            if (current_value<CURRENT_LIMIT)
            {
                float diff = (Rzad*16-R_ekv)/500.0/16.0;
                if (diff>40)
                    diff = 40;
                control_angle_int -= diff;
            }
            
            float new_angle = 184 - (Rzad*16-R_ekv)/300.0 + 0.4*control_angle_int;
            
            if (new_angle>184.0)
                new_angle = 184.0;
            if (new_angle<30.0)
                new_angle = 30.0;
            
            control_angle = new_angle;
            
            CDC_Transmit_FS((uint8_t *)&current_value, sizeof(uint16_t));
        }
        
        current_accum = 0;
        current_accum_counter = 0;
        
        voltage_accum = 0;
        voltage_accum_counter = 0;
        
        simistor_counter = 0;
        HAL_GPIO_TogglePin(GPIOA,GPIO_PIN_5);
    }
    
    if ((simistor_counter>control_angle)&&(simistor_counter<=control_angle+10))
        HAL_GPIO_WritePin(GPIOA,GPIO_PIN_2,GPIO_PIN_SET);
    else
        HAL_GPIO_WritePin(GPIOA,GPIO_PIN_2,GPIO_PIN_RESET);
    
    
    simistor_counter++;
    
    prev_ADCvalue = g_ADCValue;
    prev_ADCCurrent = g_ADCCurrent;
}

